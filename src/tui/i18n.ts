const TEXT_CATALOG: Readonly<Record<UiLanguage, Readonly<Record<UiTextKey, string>>>> = {
  en: EN_TEXT,
  'zh-CN': ZH_CN_TEXT,
};

export function resolveUiLanguage(
  preference: UiLanguagePreference,
  systemLocale: string,
): UiLanguage {
  if (preference === 'en' || preference === 'zh-CN') return preference;
  return systemLocale.trim().toLowerCase().startsWith('zh') ? 'zh-CN' : 'en';
}

export function uiText(language: UiLanguage, key: UiTextKey): string {
  return TEXT_CATALOG[language][key];
}
