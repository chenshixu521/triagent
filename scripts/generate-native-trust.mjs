#!/usr/bin/env node
/**
 * Compute helper digest/length/PE machine from the stable published exe and
 * generate src/process/generated-native-helper-trust.ts BEFORE tsup so constants
 * embed into dist/cli.js.
 *
 * Trust material is deterministic hash/length/machine only — no absolute paths
 * or wall-clock timestamps in the generated source.
 */
import { createHash } from 'node:crypto';
import {
  existsSync,
  lstatSync,
  openSync,
  readFileSync,
  readSync,
  closeSync,
  writeFileSync,
  mkdirSync,
  realpathSync,
} from 'node:fs';
import { dirname, join, normalize, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

import { acquireBuildLock } from './lib/build-lock.mjs';

const IMAGE_FILE_MACHINE_AMD64 = 0x8664;
const root = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const publishExe = join(
  root,
  'native',
  'TriAgent.ProcessHost',
  'bin',
  'Release',
  'net10.0',
  'win-x64',
  'publish',
  'triagent-process-host.exe',
);
const outTs = join(root, 'src', 'process', 'generated-native-helper-trust.ts');

export class NativeTrustError extends Error {
  /** @param {string} message */
  constructor(message) {
    super(message);
    this.name = 'NativeTrustError';
  }
}

function pathKey(input) {
  return process.platform === 'win32'
    ? input.replaceAll('/', '\\').toLocaleLowerCase('en-US')
    : input;
}

function assertRegularFile(path, label) {
  if (!existsSync(path)) {
    throw new NativeTrustError(`${label} missing: ${path}`);
  }
  const stats = lstatSync(path);
  if (stats.isSymbolicLink() || !stats.isFile()) {
    throw new NativeTrustError(`${label} must be a regular non-reparse file: ${path}`);
  }
  try {
    const real = realpathSync.native(path);
    if (pathKey(normalize(real)) !== pathKey(normalize(path))) {
      if (pathKey(resolve(real)) !== pathKey(resolve(path))) {
        throw new NativeTrustError(
          `${label} realpath diverges: ${path} -> ${real}`,
        );
      }
    }
  } catch (error) {
    if (error instanceof NativeTrustError) throw error;
    throw new NativeTrustError(
      `${label} realpath failed: ${error instanceof Error ? error.message : String(error)}`,
    );
  }
}

function readPeMachine(path) {
  const fd = openSync(path, 'r');
  try {
    const dos = Buffer.alloc(64);
    if (readSync(fd, dos, 0, 64, 0) < 64) {
      throw new NativeTrustError('helper DOS header too short');
    }
    if (dos.toString('ascii', 0, 2) !== 'MZ') {
      throw new NativeTrustError('helper is not a PE executable (missing MZ)');
    }
    const peOffset = dos.readUInt32LE(0x3c);
    const pe = Buffer.alloc(6);
    if (readSync(fd, pe, 0, 6, peOffset) < 6) {
      throw new NativeTrustError('helper PE header unreadable');
    }
    if (pe.toString('ascii', 0, 4) !== 'PE\0\0') {
      throw new NativeTrustError('helper is not a PE executable (missing PE signature)');
    }
    return pe.readUInt16LE(4);
  } finally {
    closeSync(fd);
  }
}

/**
 * @param {{ packageRoot?: string, sourceExe?: string, outPath?: string, skipLock?: boolean, reentrantToken?: string }} [options]
 */
export function generateNativeTrust(options = {}) {
  const packageRoot = resolve(options.packageRoot ?? root);
  const sourceExe = resolve(options.sourceExe ?? publishExe);
  const outPath = resolve(options.outPath ?? outTs);

  let lock;
  if (options.skipLock !== true) {
    lock = acquireBuildLock({
      lockRoot: packageRoot,
      reentrantToken: options.reentrantToken,
    });
  }

  try {
    assertRegularFile(sourceExe, 'published ProcessHost helper');
    const bytes = readFileSync(sourceExe);
    if (bytes.byteLength <= 0) {
      throw new NativeTrustError('published helper is empty');
    }
    const sha256 = createHash('sha256').update(bytes).digest('hex');
    const peMachine = readPeMachine(sourceExe);
    if (peMachine !== IMAGE_FILE_MACHINE_AMD64) {
      throw new NativeTrustError(
        `helper PE machine 0x${peMachine.toString(16)} is not win-x64 `
        + `(expected 0x${IMAGE_FILE_MACHINE_AMD64.toString(16)})`,
      );
    }

    const source =
      `/**\n`
      + ` * GENERATED by scripts/generate-native-trust.mjs — do not edit.\n`
      + ` * Embedded trust anchor for package-relative ProcessHost helper.\n`
      + ` * Contains only deterministic hash/length/machine (no absolute paths).\n`
      + ` */\n`
      + `export const EMBEDDED_NATIVE_HELPER_SHA256 =\n`
      + `  ${JSON.stringify(sha256)} as const;\n`
      + `export const EMBEDDED_NATIVE_HELPER_BYTE_LENGTH = ${String(bytes.byteLength)} as const;\n`
      + `export const EMBEDDED_NATIVE_HELPER_PE_MACHINE = 0x${peMachine.toString(16)} as const;\n`
      + `export const EMBEDDED_NATIVE_HELPER_PLATFORM = 'win-x64' as const;\n`
      + `export const EMBEDDED_NATIVE_HELPER_FILE_NAME =\n`
      + `  'triagent-process-host.exe' as const;\n`
      + `export const EMBEDDED_NATIVE_HELPER_RELATIVE_PATH =\n`
      + `  'dist/native/win-x64/triagent-process-host.exe' as const;\n`;

    mkdirSync(dirname(outPath), { recursive: true });
    writeFileSync(outPath, source, 'utf8');

    // Sanity: no absolute drive paths or timestamps in trust source.
    if (/[A-Za-z]:\\/.test(source) || source.includes(packageRoot)) {
      throw new NativeTrustError(
        'generated trust source must not embed machine-specific absolute paths',
      );
    }
    if (/\d{4}-\d{2}-\d{2}T/.test(source)) {
      throw new NativeTrustError(
        'generated trust source must not embed timestamps',
      );
    }

    return {
      sha256,
      byteLength: bytes.byteLength,
      peMachine,
      outPath,
    };
  } finally {
    lock?.release();
  }
}

const isDirect =
  process.argv[1] !== undefined
  && pathKey(fileURLToPath(import.meta.url)) === pathKey(resolve(process.argv[1]));

if (isDirect) {
  try {
    const result = generateNativeTrust({
      reentrantToken: process.env.TRIAGENT_BUILD_LOCK_TOKEN,
    });
    console.log(
      `Generated native trust: sha256=${result.sha256} bytes=${result.byteLength} pe=0x${result.peMachine.toString(16)}`,
    );
    console.log(`-> ${result.outPath}`);
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
